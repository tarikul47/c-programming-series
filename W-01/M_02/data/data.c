#include <stdio.h>

int main()
{
    int price = 3;
    char letter = 'a';
    float height = 2.56;
    double doubleNumber = 2.536987899;


    printf("price = %d\n", price);
    printf("letter = %c\n", letter);
    // after decimal gets 6 digit 
    printf("height = %f\n", height);
    // after decimal gets 15 digit 
    printf("doubleNumber = %lf\n", doubleNumber);
    return 0;
}
